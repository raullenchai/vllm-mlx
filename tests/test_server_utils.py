class TestExtractTokenLogprob:
    """Tests for _extract_token_logprob function."""

    def _make_logprobs(self, vocab_size=100, token_logprobs=None):
        """Create a mock mx.array with specific token logprobs."""
        arr = np.full(vocab_size, -10.0)  # low default
        if token_logprobs:
            for tid, lp in token_logprobs.items():
                arr[tid] = lp
        mock = MagicMock()
        mock.astype.return_value = mock
        return mock, arr

    def test_basic_top_k(self):
        from vllm_mlx.server import _extract_token_logprob

        mock, arr = self._make_logprobs(100, {5: -0.1, 10: -0.5, 20: -1.0})
        tok = MagicMock()
        tok.decode.side_effect = lambda ids: f"t{ids[0]}"

        with patch("numpy.array", return_value=arr):
            result = _extract_token_logprob(mock, token_id=5, tokenizer=tok, top_k=3)

        assert result.token == "t5"
        assert result.logprob == pytest.approx(-0.1)
        assert len(result.top_logprobs) == 3
        # Sorted descending
        lps = [t.logprob for t in result.top_logprobs]
        assert lps == sorted(lps, reverse=True)

    def test_top_k_capped_at_vocab(self):
        from vllm_mlx.server import _extract_token_logprob

        mock, arr = self._make_logprobs(10)
        tok = MagicMock()
        tok.decode.return_value = "x"

        with patch("numpy.array", return_value=arr):
            result = _extract_token_logprob(mock, token_id=0, tokenizer=tok, top_k=100)
        assert len(result.top_logprobs) == 10

    def test_out_of_bounds_token_id(self):
        from vllm_mlx.server import _extract_token_logprob

        mock, arr = self._make_logprobs(100)
        tok = MagicMock()
        tok.decode.return_value = "x"

        with patch("numpy.array", return_value=arr):
            result = _extract_token_logprob(mock, token_id=500, tokenizer=tok, top_k=3)
        assert result.logprob == 0.0

    def test_bytes_field_populated(self):
        from vllm_mlx.server import _extract_token_logprob

        mock, arr = self._make_logprobs(100, {5: -0.1})
        tok = MagicMock()
        tok.decode.return_value = "hello"

        with patch("numpy.array", return_value=arr):
            result = _extract_token_logprob(mock, token_id=5, tokenizer=tok, top_k=1)
        assert result.bytes == list(b"hello")

    def test_astype_called_for_conversion(self):
        from vllm_mlx.server import _extract_token_logprob

        mock, arr = self._make_logprobs(10)
        tok = MagicMock()
        tok.decode.return_value = "x"

